const fn spirv_words<const WORD_COUNT: usize>(bytes: &[u8]) -> [u32; WORD_COUNT] {
    let mut words = [0_u32; WORD_COUNT];
    let mut index = 0;
    while index < WORD_COUNT {
        let offset = index * 4;
        words[index] = u32::from_le_bytes([
            bytes[offset],
            bytes[offset + 1],
            bytes[offset + 2],
            bytes[offset + 3],
        ]);
        index += 1;
    }
    words
}

